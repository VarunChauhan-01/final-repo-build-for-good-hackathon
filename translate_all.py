def replace_in_file(filepath, replacements):
    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()
    
    # Ensure t is extracted from useTheme
    if "const { darkMode }" in content and "const { darkMode, t }" not in content:
        content = content.replace("const { darkMode } = useTheme();", "const { darkMode, t } = useTheme();")
        
    for old, new in replacements:
        content = re.sub(old, new, content)
        
    with open(filepath, "w", encoding="utf-8") as f:
        f.write(content)

